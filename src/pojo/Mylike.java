package pojo;
/*
 * 点赞
 */
public class Mylike {

	private Integer id;
	private Integer teamid;//用户ID
	
	private Integer type=0;//点赞的类型，0资讯
	private Integer likeid;//所点赞的ID
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	public Integer getTeamid() {
		return teamid;
	}
	public void setTeamid(Integer teamid) {
		this.teamid = teamid;
	}
	public Integer getLikeid() {
		return likeid;
	}
	public void setLikeid(Integer likeid) {
		this.likeid = likeid;
	}
	
	
	
}
